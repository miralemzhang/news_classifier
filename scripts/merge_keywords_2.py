def merge_more():
    with open('configs/keywords.json', 'r', encoding='utf-8') as f:
        keywords = json.load(f)
    
    for category, kw_dict in EXTRA_KEYWORDS_2.items():
        if category in keywords:
            for kw_type in ['strong', 'weak']:
                if kw_type in kw_dict:
                    existing = set(keywords[category].get(kw_type, []))
                    new_kws = [kw for kw in kw_dict[kw_type] if kw not in existing]
                    keywords[category][kw_type] = keywords[category].get(kw_type, []) + new_kws
                    print(f"{category}.{kw_type}: 新增 {len(new_kws)} 个")
    
    with open('configs/keywords.json', 'w', encoding='utf-8') as f:
        json.dump(keywords, f, ensure_ascii=False, indent=4)
    
    total = 0
    print("\n=== 最终关键词统计 ===")
    for cat, kd in keywords.items():
        s, w = len(kd.get('strong', [])), len(kd.get('weak', []))
        t = s + w
        total += t
        print(f"{cat}: 强={s}, 弱={w}, 合计={t}")
    print(f"\n🎉 总计: {total} 个关键词")

if __name__ == '__main__':
    merge_more()
